/**
 * Get brand reference based on business type and style
 */
export function getBrandReference(businessType: string, style: string): any {
  
  // Exact matches
  const businessBrands: Record<string, string[]> = {
    'tech': ['apple', 'google', 'tesla'],
    'fashion': ['chanel', 'nike', 'louis_vuitton', 'gucci'],
    'luxury': ['chanel', 'louis_vuitton', 'mercedes_benz'],
    'food_beverage': ['coca_cola', 'starbucks', 'red_bull'],
    'automotive': ['tesla', 'mercedes_benz'],
    'beauty': ['glossier', 'dove']
  };
  
  const brands = businessBrands[businessType] || [];
  
  // Style-based selection
  if (style.includes('minimal')) {
    return BRAND_DESIGN_DNA.apple || BRAND_DESIGN_DNA.chanel;
  }
  
  if (style.includes('bold') || style.includes('energy')) {
    return BRAND_DESIGN_DNA.nike || BRAND_DESIGN_DNA.red_bull;
  }
  
  if (style.includes('luxury')) {
    return BRAND_DESIGN_DNA.louis_vuitton || BRAND_DESIGN_DNA.chanel;
  }
  
  // Return first matching brand or apple as default
  return brands.length > 0 ? BRAND_DESIGN_DNA[brands[0]] : BRAND_DESIGN_DNA.apple;
}

/**
 * Get trend recommendation based on audience and goal
 */
export function getTrendRecommendation(targetAudience: string, goal: string): any {
  
  if (targetAudience.includes('Gen Z') || targetAudience.includes('youth')) {
    return DESIGN_TRENDS_2025.y2k_revival;
  }
  
  if (targetAudience.includes('eco') || targetAudience.includes('sustainable')) {
    return DESIGN_TRENDS_2025.sustainable_earthy;
  }
  
  if (goal === 'brand_awareness' || goal === 'attention') {
    return DESIGN_TRENDS_2025.maximal_chaos_controlled;
  }
  
  if (goal === 'luxury' || goal === 'premium') {
    return DESIGN_TRENDS_2025.holographic_iridescent;
  }
  
  // Default: organic blob shapes (safe modern choice)
  return DESIGN_TRENDS_2025.organic_blob_shapes;
}

/**
 * Get composition pattern based on intent
 */
export function getCompositionPattern(sophistication: string, energy: string): any {
  
  if (sophistication === 'high' && energy === 'low') {
    return COMPOSITIONAL_PATTERNS.negative_space_dominance;
  }
  
  if (sophistication === 'high') {
    return COMPOSITIONAL_PATTERNS.fibonacci_spiral;
  }
  
  if (energy === 'high') {
    return COMPOSITIONAL_PATTERNS.diagonal_dynamic;
  }
  
  // Balanced default
  return COMPOSITIONAL_PATTERNS.divine_proportion_grid;
}
